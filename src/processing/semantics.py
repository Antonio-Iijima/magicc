class File:
    def __init__(self, path: str = None):
        """Resolve and compile syntax files. 
        Search for semantics.py in `path/<implementation>/`;
        if not found, fall back to `path/`."""

        main = get_config("paths", "language")

        if path: 
            self.type = "DEPENDENCY"
            self.path = path
        else:
            self.type = "MAIN"
            self.path = main

        implementation = get_config("implementation")
        self.file = f"{self.path}/{implementation}/semantics.py"
        if not os.path.exists(self.file):
            self.file = f"{self.path}/semantics.py"

            if (self.type == "MAIN") and os.path.exists(self.file): 
                print_warnings(
                    msg=f"using default semantics",
                    log={"may not be implementation-specific" : [self.path]}
                )


    def compile(self) -> str:
        if not os.path.exists(self.file):
            Eval.WARNINGS[self.type.lower()].append(self.path)
            return ""
    
        with open(self.file) as file:
            Eval.LITERAL = False
            text = self.process(file.read())
            return f"""


### {self.type} : {self.path} ###



""" + text
        

    def process(self, text: str) -> str:

        def replace_fname(match: re.Match) -> str:
            return \
                pathToFunc(self.path if self.type == "DEPENDENCY" else "main") \
                + match.group().split("p_", 1)[1].lower()

        return re.sub(
            pattern=r"p_.*\(",
            repl=replace_fname,
            string=text
        )
